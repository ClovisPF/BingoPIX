package com.example.bingoexpress;

public class TConfigSorteio {
    int arrecadado = 0, bolasSorteadas = 0, bonus = 0, ganhadores = 0,
    participantes = 0, totalCartelas = 0, valorCartela = 0, valorPremio = 0;
    String titulo, data, hora, status;

    public TConfigSorteio() {
    }
    public int getArrecadado() {
        return arrecadado;
    }
    public void setArrecadado(int arrecadado) {
        this.arrecadado = arrecadado;
    }
    public int getBolasSorteadas() {
        return bolasSorteadas;
    }
    public void setBolasSorteadas(int bolasSorteadas) {
        this.bolasSorteadas = bolasSorteadas;
    }
    public int getBonus() {
        return bonus;
    }
    public void setBonus(int bonus) {
        this.bonus = bonus;
    }
    public int getGanhadores() {
        return ganhadores;
    }
    public void setGanhadores(int ganhadores) {
        this.ganhadores = ganhadores;
    }
    public int getParticipantes() {
        return participantes;
    }
    public void setParticipantes(int participantes) {
        this.participantes = participantes;
    }
    public int getTotalCartelas() {
        return totalCartelas;
    }
    public void setTotalCartelas(int totalCartelas) {
        this.totalCartelas = totalCartelas;
    }
    public int getValorCartela() {
        return valorCartela;
    }
    public void setValorCartela(int valorCartela) {
        this.valorCartela = valorCartela;
    }
    public int getValorPremio() {
        return valorPremio;
    }
    public void setValorPremio(int valorPremio) {
        this.valorPremio = valorPremio;
    }
    public String getTitulo() {
        return titulo;
    }
    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }
    public String getData() {
        return data;
    }
    public void setData(String data) {
        this.data = data;
    }
    public String getHora() {
        return hora;
    }
    public void setHora(String hora) {
        this.hora = hora;
    }
    public String getStatus() {
        return status;
    }
    public void setStatus(String status) {
        this.status = status;
    }

}


